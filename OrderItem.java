
public class OrderItem 
{
    int itemNum;
    String itemName;
    double price;
    int quantity;
    
    //Stores Orders qualities
    OrderItem(int itemNum, String itemName, double price, int quantity)
    {
        this.itemNum = itemNum;
        this.itemName = itemName;
        this.price = price;
        this.quantity = quantity;

    }


}
